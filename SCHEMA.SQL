-- DEPARTMENTS 
CREATE TABLE DEPARTMENTS (
DEPT_ID INT PRIMARY KEY AUTO_INCREMENT,
DEPT_NAME VARCHAR(30) UNIQUE,
LOCATION VARCHAR(30) NOT NULL
);

-- ROLES
CREATE TABLE ROLES (
ROLE_ID INT PRIMARY KEY AUTO_INCREMENT,
ROLES VARCHAR(30) 
);

-- EMPLOYEES
CREATE TABLE EMPLOYEES (
EMP_ID INT PRIMARY KEY AUTO_INCREMENT,
EMP_NAME VARCHAR(30) NOT NULL,
DEPT_ID INT NOT NULL,
ROLE_ID INT NOT NULL,
EMAIL VARCHAR(30) UNIQUE CHECK(EMAIL LIKE '%@%'),
HIREDATE DATE,
MGR_ID INT,
CONSTRAINT DEPT_ID_FK FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENTS(DEPT_ID),
CONSTRAINT ROLE_ID_FK FOREIGN KEY(ROLE_ID) REFERENCES ROLES(ROLE_ID)
);

-- SALARY
CREATE TABLE SALARY (
SAL_ID INT PRIMARY KEY AUTO_INCREMENT,
EMP_ID INT UNIQUE,
BASE_SALARY DECIMAL(7,2),
BONUS DECIMAL(6,2),
EFFECTIVE_DATE DATE,
CONSTRAINT EMP_ID_FK FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEES(EMP_ID)
);